def decode_char(char)
  char.strip!
  MORSE_CODE[char] || ' '
end

# Method for decoding a word
def decode_word(word)
  word.strip.split(/[ \t]+/).map { |c| decode_char(c) }.join
end

# Method for decoding a sentence
def decode_message(message)
  message.strip!
  message.split('  ').map { |word| decode_word(word) }.join(' ')
end
